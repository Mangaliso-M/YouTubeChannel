package javaIntroduction;

public class Loops {
	public static void main(String[] args) {
		
		//do-while loops
		//execute statement(s) at least once and then stopping when a certain condition is met
		//consider the terminating condition.
		
	    int y = 6;
		
		do {
			
			System.out.println("y = "+ y);
			
			y ++; 
		}while(y < 9);
		
	}
}
